package forum;

public class Forum {
	
	private int forumID;
	private String forumTitle;
	private String userID;
	private String forumDate;
	private String forumContent;
	private int forumAvailable;
	public int getForumID() {
		return forumID;
	}
	public void setForumID(int forumID) {
		this.forumID = forumID;
	}
	public String getForumTitle() {
		return forumTitle;
	}
	public void setForumTitle(String forumTitle) {
		this.forumTitle = forumTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getForumDate() {
		return forumDate;
	}
	public void setForumDate(String forumDate) {
		this.forumDate = forumDate;
	}
	public String getForumContent() {
		return forumContent;
	}
	public void setForumContent(String forumContent) {
		this.forumContent = forumContent;
	}
	public int getForumAvailable() {
		return forumAvailable;
	}
	public void setForumAvailable(int forumAvailable) {
		this.forumAvailable = forumAvailable;
	}
	
	
	
	
}
